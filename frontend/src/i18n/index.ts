// Simple translation function
const translations = { en, de, ro, fr, es, it, pt, nl, sv, ru, zh, ja, dr, ps, ar };

let currentLanguage = "en";
let languageChangeListeners: (() => void)[] = [];

export const setLanguage = (lang: string) => {
  currentLanguage = lang;
  localStorage.setItem("language", lang);
  // Notify all listeners
  languageChangeListeners.forEach((listener) => listener());
};

export const getLanguage = () => {
  const saved = localStorage.getItem("language");
  if (saved && translations[saved as keyof typeof translations]) {
    currentLanguage = saved;
  }
  return currentLanguage;
};

export const addLanguageChangeListener = (listener: () => void) => {
  languageChangeListeners.push(listener);
  return () => {
    languageChangeListeners = languageChangeListeners.filter(
      (l) => l !== listener
    );
  };
};

export const t = (key: string): string => {
  const keys = key.split(".");

  const resolve = (lang: keyof typeof translations) => {
    let v: any = translations[lang].translation;
    for (const k of keys) {
      v = v?.[k];
      if (v === undefined || v === null) return undefined;
    }
    return v;
  };

  const primary = resolve(currentLanguage as keyof typeof translations);
  if (primary !== undefined) return primary as string;

  // Fallback to English when key missing in current language
  const fallback = resolve('en');
  return (fallback as string) || key;
};

